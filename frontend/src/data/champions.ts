export type ChampionName = (typeof CHAMPIONS)[number];

const CHAMPION_IMAGE_VERSION = "16.14.1";

const CHAMPION_IMAGE_KEY_OVERRIDES: Partial<Record<ChampionName, string>> = {
  "Bel'Veth": "Belveth",
  "Cho'Gath": "Chogath",
  "Kai'Sa": "Kaisa",
  "Kha'Zix": "Khazix",
  LeBlanc: "Leblanc",
  "Nunu & Willump": "Nunu",
  "Renata Glasc": "Renata",
  "Vel'Koz": "Velkoz",
  Wukong: "MonkeyKing",
};

export function isChampionName(value: string): value is ChampionName {
  return CHAMPIONS.some((champion) => champion.toLowerCase() === value.trim().toLowerCase());
}

export function canonicalChampionName(value: string): ChampionName | null {
  return CHAMPIONS.find((champion) => champion.toLowerCase() === value.trim().toLowerCase()) ?? null;
}

export function getChampionImageUrl(value: string): string | null {
  const champion = canonicalChampionName(value);
  if (!champion) {
    return null;
  }
  const imageKey = CHAMPION_IMAGE_KEY_OVERRIDES[champion] ?? champion.replace(/[^a-zA-Z0-9]/g, "");
  return `https://ddragon.leagueoflegends.com/cdn/${CHAMPION_IMAGE_VERSION}/img/champion/${imageKey}.png`;
}

export function normalizeChampionName(value: string): string {
  return value.trim().toLowerCase();
}
